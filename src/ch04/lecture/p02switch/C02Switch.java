package ch04.lecture.p02switch;

public class C02Switch {
	public static void main(String[] args) {
		// switch문 종료를 위해서는 break;를 사용해야한다
		
		int val = 1;
		
		switch(val) {
		case 1:
			System.out.println("code 1");
		case 2:
			System.out.println("code 2");
		case 3:
			System.out.println("ccode 3");
		}
	}

}
